      'pt' => 'Portuguese',
      'ro' => 'Romanian',
      'ru' => 'Russian',
      'sk' => 'Slovak',
      'sv' => 'Swedish',
      'tg' => 'Tajik',
      'te' => 'Telugu',
      'th' => 'Thai',
      'tl' => 'Filipino',
      'tr' => 'Turkish',
      'uk' => 'Ukrainian',
      'uz' => 'Uzbek',
      _ => raw,
    };

    if (region == null || region.isEmpty) {
      return languageName;
    }

    final regionName = switch (region) {
      'VN' => 'Vietnam',
      'US' => 'United States',
      'GB' => 'United Kingdom',
      'AU' => 'Australia',
      'CA' => 'Canada',
      'IN' => 'India',
